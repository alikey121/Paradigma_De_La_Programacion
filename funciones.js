function Suma(x) {
    let accion = x + 5;
    return accion;
};

console.log(Suma(7));

function Resta(x) {
    let accion = x - 5;
    return accion;
};

console.log(Resta(7));

function Multiplicacion(x) {
    let accion = x * 5;
    return accion;
};

console.log(Multiplicacion(7));

function Division(x) {
    let accion = x / 5;
    return accion;
};

console.log(Division(7));

